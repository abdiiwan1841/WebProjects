<h3>Project Description:</h3>
<p>
This project is a simple educational institution based time table management system. The project includes the following features:
<ol>
<li>Administration control with secured login system</li>
<li>Faculties panel with secured login system</li>
<li>Schedule Management with automated time intervals</li>
<li>Individual class schedule updates</li>
<li>Branch/Department and Subjects management</li>
<li>Faculties Registration with validation process</li>
<li>Tabulated class schedule with period, time &amp; days label</li>
<li>Tabulated Subjects &amp; Faculties details for individual departments.</li>
<li>Tabulated class schedule for individual faculty</li>
</ol>
</p>
